    windows=(16 32 64 188 256 320 512 1024)
    cascades=(1 2 3 5)

    interps=(1)
    decimates=(1)

    for (( typeI=0; typeI<${#types[@]}; typeI+=2 )); do
        echo "${types[$typeI]},${types[`expr $typeI + 1 `]}"
        dType=${types[$typeI]}
        cType=${types[`expr $typeI + 1 `]}
        # Scrape int number from coef type
        shift=${cType#*int}
        if [[ $cType == *float ]]; then
            shift=0
        fi
        redLengths=(${lengths[@]})
        reduced=0
        redCascades=(${cascades[@]})
        if ! [[ ( "$dType" == "cint16" ) && ( "$cType" == "int16" ) ]]; then
            reduced=1
            redLengths=(16 64 240)
            redCascades=(1 3)
        fi
        for fLen in "${redLengths[@]}"; do
            for iRate in "${interps[@]}"; do
                for dRate in "${decimates[@]}"; do
                    for cLen in "${redCascades[@]}"; do
                        redWindows=(${windows[@]})
                        # to reduce number of tests, limited number of windows
                        # for cascades
                        if [[ ( $cLen -gt 1 ) || ( $reduced == 1 ) ]]; then
                            redWindows=(256 1024)
                        fi
                        # Always use a window of FIR Len
                        test_arr[$LINENO]="make all DATA_TYPE=$dType COEFF_TYPE=$cType FIR_LEN=$fLen CASC_LEN=$cLen SHIFT=$shift INPUT_WINDOW_VSIZE=$fLen"
                        #echo ${redWindows[@]}
                        for wSize in "${redWindows[@]}"; do
                            if [ $wSize -gt $fLen ]
                            then
                                #echo "DATA_TYPE=$dType COEFF_TYPE=$cType FIR_LEN=$fLen  CASC_LEN=$cLen SHIFT=$shift INPUT_WINDOW_VSIZE=$wSize"
                                test_arr[$LINENO]="make all DATA_TYPE=$dType COEFF_TYPE=$cType FIR_LEN=$fLen CASC_LEN=$cLen SHIFT=$shift INPUT_WINDOW_VSIZE=$wSize"
                                #echo "wSize $wSize"
                            fi
                        done #wSize
                    done #cLen
                done #dRate
            done #iRate
        done #fLen
    done #typeI
fi


